<script type="text/javascript">

    function showLoader() {
        $('#de-loader').css('display','flex');
    }

    // Function to hide the loader
    function hideLoader() {
        $('#de-loader').hide();
    }


    function alert_msg(title,message,icon) {
        swal({
            title: title,
            text: message,
            icon: icon,
        });
    }

    $(document).on('submit','#contact_form',function(e){
        e.preventDefault();
        // console.log($(this).files);
          var form = $(this)[0];
          var form_data = new FormData(form);
          form_data.append('Action','contact_submit');  
            $.ajax({
                url: 'common_ajax.php',
                type: 'POST',
                data:  form_data,
                dataType: 'json',
                processData: false,
                contentType: false,
                beforeSend: function(){
                    showLoader();
                },
                success: function(response) {
                    if(response.status == 200) {
                        swal({
                                icon: "success",
                                title: "Success!",
                                text: response.message,
                                button: true 
                            }).then((result) => {
                                if (result) {
                                    location.reload(true);
                                }
                        });

                    } else {
                        alert_msg('Failed',response.message,'error');
                    }
                },
                complete:function() {
                    hideLoader();
                }
            });

    });
</script>